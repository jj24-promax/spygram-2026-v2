export function normalizeNameKey(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z]/g, '');
}

export function buildNameSet(names: readonly string[], aliases: readonly string[] = []): Set<string> {
  const set = new Set<string>();
  for (const name of names) {
    set.add(normalizeNameKey(name));
  }
  for (const alias of aliases) {
    set.add(normalizeNameKey(alias));
  }
  return set;
}

